import numpy as np


def Lattice_Basis(a:str, N:int = 0) -> np.array:
    if a == 'Z':
        if N <= 0:
            print('please input dimension of Z lattice')
        else:
            m = np.identity(N)
        pass
    elif a == 'A2':
        m = np.array([[pow(3, 1/2)/2, 0],
                      [0.5,           1]])
        pass
